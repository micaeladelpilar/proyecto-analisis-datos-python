# Script básico que calcula las ventas mensuales


# Aseguro que exista la columna 'importe'
if 'importe' not in ventas.columns:
    ventas['importe'] = ventas['precio'] * ventas['cantidad']

# Creo un diccionario acumulador
totales = {} # esto servira como acumulador donde se iran guardando las ventas totales por año y mes

# Contadores de control
procesadas = 0 # cuenta cuantas filas validas de analizan
omitidas = 0 # cantidad de filas saltadas por datos faltantes

# Recorro el DataFrame fila por fila
for fila in ventas.itertuples(index=False): # recorre le df fila por fila
    fecha = fila.fecha_venta
    importe = fila.importe

 # Valido que la fila tenga datos válidos
    if pd.isna(fecha) or pd.isna(importe): # si hay valores nulos el contador omitidos se incrementa en 1
        omitidas += 1
        continue

    año = fecha.year # a partir del objeto datatime, extraigo el año y mes
    mes = fecha.month # creoclave única combinando ambos valores (año, mes).

    clave = (año, mes) #Esa clave servirá para agrupar las ventas del mismo mes y año.

    # Acumulo los importes por (año, mes)
    if clave in totales:          # Si la clave (año, mes) ya existe en el diccionario totales, se le suma el importe de la fila actual.
        totales[clave] += importe
    else:
        totales[clave] = importe # Si no existe, se crea una nueva entrada con el importe inicial.

    procesadas += 1              # se incrementa el contador de procesadas.

# Ordeno las claves (año, mes)
totales_ordenados = dict(sorted(totales.items())) # el sorted () sirve para ordenar el diccionario por sus claves, haciendo que aparezcan cronologicamente

# Muestro los resultados
print("---- Ventas mensuales ---- \n")
for (año, mes), total in totales_ordenados.items():
    print(f"{año}-{mes:02d}: ${total:,.2f}") # el :02d es para que sean con dos digitos y :,.2f formatea el número con comas para miles y dos decimales.

print("\n---- Resumen ----")
print("Filas procesadas:", procesadas)
print("Filas omitidas por datos faltantes:", omitidas)

