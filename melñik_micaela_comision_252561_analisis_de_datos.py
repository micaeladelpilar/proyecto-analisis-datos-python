"""# Etapa 2: Actividad 2
Transformación de Datos: Aplicar filtros y transformaciones para crear una
tabla de ventas que muestre solo los productos con alto rendimiento.
* Elegí filtrar los productos por encima del promedio de todos los ingresos
"""

# ----Paso 1: Calcular ingresos totales por producto----

# Creé un diccionario acumulador: clave = producto, valor = ingresos totales
ingresos_por_producto = {}

for fila in ventas.itertuples(index=False): # Recorro el df fila por fila. En cada iteración, se extraen los valores de las columnas producto e importe de la fila actual.
    producto = fila.producto
    importe = fila.importe

    if pd.isna(producto) or pd.isna(importe):
        continue  # hago una validación de calidad de datos. omito filas inválidas. si es NaN omito esa fila y paso a la siguiente

    if producto in ingresos_por_producto: # Si el producto ya existe como clave en el diccionario, se suma su nuevo importe al total acumulado.
        ingresos_por_producto[producto] += importe
    else:                                 # Si es la primera vez que aparece ese producto, se crea la clave y se le asigna el importe inicial.
        ingresos_por_producto[producto] = importe

# ----Paso 2: Convertir a DataFrame para filtrar y ordenar----

tabla_ingresos = pd.DataFrame( # convierto el diccionario a una lista de tuplas (producto, total) para poder transformarlo fácilmente en un DataFrame de Pandas.
    [(prod, total) for prod, total in ingresos_por_producto.items()],
    columns=['producto', 'ingresos_totales']
)

# Se ordena de mayor a menor
tabla_ingresos = tabla_ingresos.sort_values(by='ingresos_totales', ascending=False) #los productos con mayores ventas aparecen primero.

# ----Paso 3: Filtrar productos de alto rendimiento----

# Considero "alto rendimiento" los que tienen ingresos > promedio
promedio_ingresos = tabla_ingresos['ingresos_totales'].mean() #Calculo el promedio general de ingresos (mean()).
tabla_alto_rendimiento = tabla_ingresos[tabla_ingresos['ingresos_totales'] > promedio_ingresos]


# ----Paso 4: Mostrar resultados----

print("---- Productos de alto rendimiento ----")
display(tabla_alto_rendimiento)

"""# Etapa 2: Actividad 3
Agregación: Resumir las ventas por categoría de producto y analizar los
ingresos generados.
"""

# ----Paso 1: Agrupar y resumir por categoría----

resumen_cat = (
    ventas.groupby('categoria', as_index=False)
          .agg(
              ingresos=('importe', 'sum'),        # se suma dinero por categoría
              unidades=('cantidad', 'sum'),       # se suma unidades vendidas
              transacciones=('id_venta', 'count') # cantidad de ventas
          )
          .sort_values('ingresos', ascending=False) # orden de mayor a menor ingresos
)

# ----Paso 2: Calcular participación % sobre el total----

total_ingresos = resumen_cat['ingresos'].sum() # calculo la suma total de ingresos
resumen_cat['participacion_%'] = (resumen_cat['ingresos'] / total_ingresos * 100).round(2) # porcentaje que representa cada categoria y con el .round(2) lo redondea a dos digitos

# ----Paso 3: Calcular ticket promedio por transacción----

resumen_cat['ticket_promedio'] = (resumen_cat['ingresos'] / resumen_cat['transacciones']).round(2)

# ----Paso 4: Mostrar resultado----

print("---- Resumen de ventas por categoría ----")
display(resumen_cat)

